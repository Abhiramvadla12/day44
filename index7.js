//named function:
function isPositive(x) { 
    return x > 0;
    }
console.log(isPositive(10)) ;
//anonymous function:
var isPositive = function (x){
    return x > 0;
}
console.log(isPositive(-10)) ;
//arrow function :
var isPositive = (x) =>{
    return x >0;
}  
console.log(isPositive(348546846)) ;